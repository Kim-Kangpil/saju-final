def get_twelve_state(cheongan: str, jiji: str) -> str:
    if cheongan in TWELVE_STATES_MAP:
        return TWELVE_STATES_MAP[cheongan].get(jiji, "")
    return ""

def calculate_twelve_states(pillars: dict) -> dict:
    result = {}
    for name, pillar in pillars.items():
        cheongan = pillar.get("cheongan", "")
        jiji = pillar.get("jiji", "")
        if cheongan and jiji:
            result[name] = get_twelve_state(cheongan, jiji)
    return result
