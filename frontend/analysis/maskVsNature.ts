interface MaskVsNatureResult {
  text: string;
}

const pick = <T,>(v: T | undefined, fallback: T) => (v === undefined ? fallback : v);

function joinLines(lines: string[]) {
  return lines.filter(Boolean).join("\n\n");
}

function toPolite(str: string) {
  return str
    .replace(/나\./g, "요.")
    .replace(/야\./g, "요.")
    .replace(/야$/g, "요")
    .replace(/해\./g, "해요.")
    .replace(/해$/g, "해요")
    .replace(/거야\./g, "거예요.")
    .replace(/거야$/g, "거예요")
    .replace(/있어\./g, "있어요.")
    .replace(/있어$/g, "있어요")
    .replace(/보여\./g, "보여요.")
    .replace(/보여$/g, "보여요")
    .replace(/필요해\./g, "필요해요.")
    .replace(/필요해$/g, "필요해요")
    .replace(/쉬워\./g, "쉬워요.")
    .replace(/쉬워$/g, "쉬워요")
    .replace(/나와\./g, "나와요.")
    .replace(/나와$/g, "나와요")
    .replace(/맞아\./g, "맞아요.")
    .replace(/맞아$/g, "맞아요")
    .replace(/이야\./g, "이에요.")
    .replace(/이야$/g, "이에요")
    .replace(/이라\./g, "이라서요.")
    .replace(/올라와\./g, "올라와요.")
    .replace(/올라와$/g, "올라와요");
}

