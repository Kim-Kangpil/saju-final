/** 말투 변환: 기준은 격식체(합니다). empathy=해요/편이에요, fun=해/편이야 */
function toTone(text: string, tone: CharKey): string {
  if (tone === "reality") return text;
  if (tone === "empathy") {
    return text
      .replace(/합니다\./g, "해요.")
      .replace(/있습니다\./g, "있어요.")
      .replace(/나타납니다\./g, "나타나요.")
      .replace(/나타나는 편입니다/g, "나타나는 편이에요")
      .replace(/드러납니다/g, "드러나요")
      .replace(/나타납니다/g, "나타나요")
      .replace(/편입니다/g, "편이에요")
      .replace(/타입입니다/g, "타입이에요")
      .replace(/경향이 있습니다/g, "경향이 있어요");
  }
  if (tone === "fun") {
    return text
      .replace(/합니다\./g, "해.")
      .replace(/있습니다\./g, "있어.")
      .replace(/나타납니다\./g, "나타나.")
      .replace(/나타나는 편입니다/g, "나타나는 편이야")
      .replace(/드러납니다/g, "드러나")
      .replace(/나타납니다/g, "나타나")
      .replace(/편입니다/g, "편이야")
      .replace(/타입입니다/g, "타입이야")
      .replace(/경향이 있습니다/g, "경향이 있어");
  }
  return text;
}

interface MaskVsNatureResult {
  text: string;
}

/**
 * 겉모습과 속마음 해석 — 최종 완전판
 * 1문단: 월간+월지 조합 + 십이운성 수식어
 * 2문단: 시간 기질 + 시지 무의식
 * 3문단: 월간 vs 시간 계열 종합
 */
export function analyzeMaskVsNature(
  monthStemTenGod: string,
  monthBranchTenGod: string,
  monthTwelveState: string,
  hourStemTenGod: string,
  hourBranchTenGod: string,
  selectedChar: CharKey
): MaskVsNatureResult {
  const t = selectedChar;
  const branchCat = monthBranchToCat(monthBranchTenGod || monthStemTenGod);
  const p1Key = `${monthStemTenGod}-${branchCat}`;
  const p1Base = pick(P1_COMBO[p1Key], P1_COMBO[`${monthStemTenGod}-인성`] ?? "사회에서 일정한 이미지와 행동 방식이 드러나는 편입니다.");
  const strength = pick(TWELVE_STRENGTH[monthTwelveState], "mid");
  const modifier = STRENGTH_MODIFIER[strength];
  const p1 = toTone(`${modifier} ${p1Base}`, t);

  const hourNature = pick(HOUR_STEM_NATURE[hourStemTenGod], "편한 자리에서 내면의 성향이 드러나는 편입니다.");
  const hourPattern = pick(HOUR_BRANCH_PATTERN[hourBranchTenGod || hourStemTenGod], "무의식적으로 반복되는 패턴이 있습니다.");
  const p2 = toTone(`${hourNature} ${hourPattern}`, t);

  const monthSeries = pick(TEN_GOD_SERIES[monthStemTenGod], "안정형");
  const hourSeries = pick(TEN_GOD_SERIES[hourStemTenGod], "안정형");
  const p3Key = monthSeries === hourSeries ? "same" : `${monthSeries}-${hourSeries}`;
  const p3Base = p3Key === "same" ? SYNTHESIS_SAME : pick(SYNTHESIS_CROSS[p3Key], SYNTHESIS_SAME);
  const p3 = toTone(p3Base, t);

  let out = [p1, p2, p3].join("\n\n");
  if (out.length > 700) {
    out = out.slice(0, 680).trim();
    const last = out.lastIndexOf(".");
    if (last > 550) out = out.slice(0, last + 1);
  }
  if (out.length < 600) {
    const extra = " 시간이 지날수록 이 성향이 더 분명하게 드러나는 편입니다.";
    out = (out + extra).slice(0, 700).trim();
    const last = out.lastIndexOf(".");
    if (last > 550) out = out.slice(0, last + 1);
  }
  return { text: out };
}
