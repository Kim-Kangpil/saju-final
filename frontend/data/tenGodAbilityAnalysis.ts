/** 문장 경계(마침표+공백) 근처에서 잘라 2문단으로 나눔. */
function splitAtSentenceBoundary(text: string, nearCharIndex: number): [string, string] {
  const start = Math.max(0, nearCharIndex - 50);
  let idx = -1;
  for (let i = start; i < Math.min(text.length, nearCharIndex + 50); i++) {
    if (text.slice(i).startsWith(". ") || text.slice(i).startsWith(".요")) {
      idx = text.slice(i).startsWith(".요") ? i + 2 : i + 1;
      break;
    }
  }
  if (idx <= 0) idx = Math.min(nearCharIndex, text.length - 1);
  const a = text.slice(0, idx).trim();
  const b = text.slice(idx).trim();
  return [a, b];
}

/**
 * 십성으로 보는 주요 능력 문단. 2개 이상 있는 십신 위주로, 현실적 능력·발현·활용. 총 ~600자, 3문단.
 */
export function getTenGodAbilityParagraph(
  pillars: SajuPillarsForTenGod,
  tone: TenGodAbilityToneKey
): string {
  const count = countTenGods(pillars);
  const order: string[] = ["비견", "겁재", "식신", "상관", "편재", "정재", "편관", "정관", "편인", "정인"];
  const candidates = order.filter((tg) => (count[tg] ?? 0) >= 2);
  const sorted = [...candidates].sort((a, b) => (count[b] ?? 0) - (count[a] ?? 0));
  const take = sorted.slice(0, 2);
  const main: string[] = [];
  const useShort = take.length >= 2;
  for (const tg of take) {
    const block = useShort ? TEN_GOD_ABILITY_BODY_SHORT[tg]?.[tone] : TEN_GOD_ABILITY_BODY[tg]?.[tone];
    if (block) main.push(block);
  }

  if (main.length === 0) {
    if (tone === "empathy") return "당신 사주에서 두 개 이상 겹쳐 나오는 십성이 뚜렷하지 않아요. 여러 기운이 고르게 섞여 있어, 상황에 맞게 다양한 능력을 쓰기 좋은 구조예요. 관심 있는 분야를 하나씩 경험해 보시면 그중에서도 잘 맞는 능력이 드러날 거예요.";
    if (tone === "reality") return "십신이 고르게 분포하여 특정 영역이 두드러지지 않습니다. 상황에 따른 다양한 역량 발휘가 가능한 구조입니다.";
    return "십성이 고르게 있어서 딱 하나가 튀진 않아. 여러 거 해 보면서 잘 맞는 거 찾으면 돼.";
  }

  if (main.length === 1) {
    const mid = Math.floor(main[0].length / 2);
    const [p1, p2] = splitAtSentenceBoundary(main[0], mid);
    return [p1, p2].join("\n\n").trim();
  }
  return [main[0], main[1]].join("\n\n").trim();
}

